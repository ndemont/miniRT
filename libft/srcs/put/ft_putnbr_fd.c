#include "libft.h"

void	ft_putnbr_fd(long n, int fd)
{
	int		c;
	long	nb;

	nb = n;
	if (n < 0)
	{
		write(fd, "-", 1);
		nb = -n;
	}
	if ((nb / 10) > 0)
		ft_putnbr_fd((nb / 10), fd);
	c = (nb % 10) + 48;
	write(fd, &c, 1);
}
